public class logical_operator {
    public static void main(String[] args) {
        System.out.println("Two logical operator - && , ||");
        int x=30;
        int y=40;
        if (x <60 && y<60){
            System.out.println("both are less than 60");

        }


        int x1=30;
        int y1=70;
        if (x1 <60 || y1<60){
            System.out.println("atleast one is   less than 60");
            
        }
    }
    
}
