public class loops {
    public static void main(String[] args) {
        System.out.println("for loop");
        for( int i=0;i<50;i++)
        {
        
            System.out.println(i);
        
        }


        
        System.out.println("while loop");
        int x=0;
        while(x<=50)
        {
            System.out.println(x);
            x++;
        }

        int z=0;
        System.out.println("do while loop");
        do{
            System.out.println(z);
            z++;
        }while(z<=50);
    }
}
