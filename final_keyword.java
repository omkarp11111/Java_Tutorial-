public class final_keyword {
    public static void main(String[] args) {
        System.out.println("we can make any variable be constant -that value of can't be changed ");
        final float  PI=3.14F;
        // It is best practice to use Capital letter variable for defining the constant value 
        System.out.println(PI);
        // PI=3;    // THIS WILL SHOW ERROR ,AS WE ARE REINTIALISING THE CONSTANT VARIABLE 

    }
    
}
